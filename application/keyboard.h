
#define ESC 0x76
#define SPACE 0x29
#define ENTER 0x5A

// We are going to assign w, a, s, d as the movement keys as we would have
// to look at two scan codes for the actual arrows.
#define UP      0x1D   // w
#define DOWN    0x1B   // s
#define RIGHT   0x23   // d
#define LEFT    0x1C   // a
#define ENTER    0x5A   // a
#define ESC 0x76 //esc
#define NOP    0x00   // nop